"""
This module contains utility functions for basic arithmetic operations.
"""

def add(a: int, b: int) -> int:
    """
    Adds two integers and returns the result.

    Parameters:
    a (int): The first integer.
    b (int): The second integer.

    Returns:
    int: The sum of the two integers.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Subtracts one integer from another and returns the result.

    Parameters:
    a (int): The integer to subtract from.
    b (int): The integer to subtract.

    Returns:
    int: The difference between the two integers.
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Multiplies two integers and returns the result.

    Parameters:
    a (int): The first integer.
    b (int): The second integer.

    Returns:
    int: The product of the two integers.
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Divides one integer by another and returns the result as a float.

    Parameters:
    a (int): The integer to divide.
    b (int): The integer to divide by.

    Returns:
    float: The quotient of the two integers.
    """
    return a / b
